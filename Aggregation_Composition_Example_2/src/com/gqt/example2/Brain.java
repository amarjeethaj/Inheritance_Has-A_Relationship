package com.gqt.example2;

public class Brain {
	private String colour;
	private int bweight;
	
	public Brain(String colour, int bweight) {
		this.colour = colour;
		this.bweight = bweight;
	}

	public String getColour() {
		return colour;
	}

	public int getBweight() {
		return bweight;
	}
	
	
}
